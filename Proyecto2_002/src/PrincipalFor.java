
public class PrincipalFor {
    public static void main(String[] args) {
        
        for(int i = 1; i <= 10; i++)
        {
            System.out.println("Numero: " + i);
        }
        int x = 0;
        for(;x < 10;)
        {
            System.out.println(" valor de x: " + (++x));
        }
    }
}
